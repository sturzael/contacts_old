public class Print
{
    public String first;
    public String second;

    /**
     * Print Constructor
     * Confirms that the they are the same ones as above
     *
     * @param A String first
     * @param A String second
     */
    public Print(String first, String second)
    {
        this.first = first;
        this.second = second;
    }

    /**
     * Method fst
     *
     * @return first
     */
    public String fst(){
        return first;
    }
    
    /**
     * Method snd
     *
     * @return second
     */
    public String snd(){
        return second;
    }
    

}